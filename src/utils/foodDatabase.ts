export const searchFood = (keyword: string): Food[] => {
  if (!keyword.trim()) return [];
  const lowerKeyword = keyword.toLowerCase();
  return FOOD_DATABASE.filter(food =>
    food.name.toLowerCase().includes(lowerKeyword)
  );
};

export const getFoodsByCategory = (category: string): Food[] => {
  return FOOD_DATABASE.filter(food => food.category === category);
};

export const getLowCalorieFoods = (maxCalorie: number = 100): Food[] => {
  return FOOD_DATABASE.filter(food => food.calorie <= maxCalorie);
};

export const getHighProteinFoods = (minProtein: number = 15): Food[] => {
  return FOOD_DATABASE.filter(food => (food.protein || 0) >= minProtein);
};
